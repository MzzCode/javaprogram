package com.basic;

public class StringExample {
public static void main(String args[]) {
	
	// string by string literal
	
	String name = "mega java";
	
	// String by new keyword
	
	String name2 =  new String ("mega java 2");
	
	System.out.println("string by string literal: " +name);
	System.out.println("String by new keyword: " +name2);
	
}
}
