package com.basic;

public class IntegerLiteralExample {
public static void main(String args[]) {
	
	//Decimal-from literal
	int a =101;
	
	//octal-form literal
	int b = 0100;
	
	//hexa-decimal form
	int c = 0xFace;
	
	//binary literal
	
	int d = 0b1111;
	
	System.out.println("Decimal-form literal: " +a);
	System.out.println("octal-form literal: " +b);
	System.out.println("hexa decimal-form literal: " +c);
	System.out.println("binary-form literal: " +d);
	
}
}
