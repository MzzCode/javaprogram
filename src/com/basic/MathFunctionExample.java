package com.basic;

public class MathFunctionExample {
	public static void main(String args[]) {
		//Math.sqrt(): Method for square root of number
		System.out.println(Math.sqrt(25));
		
		//Math.abs(): return positive number
		
		System.out.println(Math.abs(-9.54));
		
		//Math.cbrt() // Method for cube root
		 double value1 = Double.POSITIVE_INFINITY;
		    double value2 = 27.0;
		    double value3 = -64;
		    double value4 = 0.0;
		    
		    System.out.println(value1);
		    System.out.println(value2);
		    System.out.println(value3);
		    System.out.println(value4);
		    
		    //Math.pow() Method a^B
		    
		    double a = 5.0;
		    double b = 3.0;
		    double c = 3.3;
		    double d = 2.5;
		    System.out.println(Math.pow(a, b));
		    System.out.println(Math.pow(c, d));
		    
		  //Math.ceil() Method
		    double e = 8.6;
		    double f = 6.3;
		    
		    System.out.println(Math.ceil(e)); //9
		    System.out.println(Math.ceil(f)); //7
		    
		//Math.floor() Method
	
		double g = 5.8;
		double h = 7.4;
		System.out.println(Math.floor(g)); //5
		System.out.println(Math.floor(h)); //7
		
		
	}

}
