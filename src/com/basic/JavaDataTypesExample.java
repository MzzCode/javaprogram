package com.basic;

public class JavaDataTypesExample {

	public static void main(String args[]) {
		
		char a = 'a';//In Java, a character is represented using single quotes(' ') And String in double quotes(" ")
		int i = 89;
		byte b =4;
		short s = 56;
		float f = 8.7333f;//at last or suffix f must be write otherwise it give error
		double d = 4.3552;
		long l = 1234211344334L;// for good practice must write at last L or l
		
	System.out.println("char: " +a);
	System.out.println("integer: " +i);
	System.out.println("byte: " +b);
	System.out.println("short: " +s);
	System.out.println("double: " +d);
	System.out.println("float: " +f);
	System.out.println("long: " +l);
	}
}
